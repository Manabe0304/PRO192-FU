public class Bai5 {
    public static void main(String[] args) {
        // Gợi ý: Khai báo mảng một chiều gồm 5 số nguyên
        int[] mangSo = {1, 2, 3, 4, 5};

        // Sử dụng vòng lặp để in các phần tử của mảng
        for (int i = 0; i < mangSo.length; i++) {
            System.out.println("Phần tử tại vị trí " + i + " là: " + mangSo[i]);
        }
    }
}
