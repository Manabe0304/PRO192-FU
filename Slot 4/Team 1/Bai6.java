public class Bai6 {
    public static void main(String[] args) {
        // Gợi ý: Khởi tạo một chuỗi và sử dụng các phương thức của lớp String
        String chuoi = "Xin chào Java";
        
        // Lấy độ dài chuỗi
        System.out.println("Độ dài của chuỗi: " + chuoi.length());

        // Lấy ký tự tại vị trí thứ 0
        System.out.println("Ký tự tại vị trí 0: " + chuoi.charAt(0));
    }
}
