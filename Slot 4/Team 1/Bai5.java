public class Bai5 {
    public static void main(String[] args) {
        // Gợi ý: Khai báo các biến số nguyên và sử dụng các toán tử số học để thực hiện phép tính
        int a = 10;
        int b = 5;

        System.out.println("Tổng: " + (a + b));  // Phép cộng
        System.out.println("Hiệu: " + (a - b));  // Phép trừ
        System.out.println("Tích: " + (a * b));  // Phép nhân
        System.out.println("Thương: " + (a / b)); // Phép chia
    }
}
