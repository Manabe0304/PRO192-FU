public class Bai1 {
    public static void main(String[] args) {
        // Gợi ý: Sử dụng các phương thức của lớp Math để thực hiện các phép toán
        double canBacHai = Math.sqrt(16); // Tính căn bậc hai
        double luyThua = Math.pow(2, 3);  // Tính lũy thừa 2^3
        double giaTriTuyetDoi = Math.abs(-5);  // Tính giá trị tuyệt đối

        // In kết quả ra màn hình
        System.out.println("Căn bậc hai của 16 là: " + canBacHai);
        System.out.println("2 lũy thừa 3 là: " + luyThua);
        System.out.println("Giá trị tuyệt đối của -5 là: " + giaTriTuyetDoi);
    }
}
